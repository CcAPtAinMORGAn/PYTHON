import math

# Example for acos(x)
x_acos = 0.5
result_acos = math.acos(x_acos)
print(f"acos({x_acos}) = {result_acos} radians")

# Example for asin(x)
x_asin = 0.5
result_asin = math.asin(x_asin)
print(f"asin({x_asin}) = {result_asin} radians")

# Example for atan(x)
x_atan = 1
result_atan = math.atan(x_atan)
print(f"atan({x_atan}) = {result_atan} radians")

# Example for atan2(y, x)
y_atan2 = 1
x_atan2 = 1
result_atan2 = math.atan2(y_atan2, x_atan2)
print(f"atan2({y_atan2}, {x_atan2}) = {result_atan2} radians")

# Example for cos(x)
x_cos = 0  # radians
result_cos = math.cos(x_cos)
print(f"cos({x_cos}) = {result_cos}")

# Example for hypot(x, y)
x_hypot = 3
y_hypot = 4
result_hypot = math.hypot(x_hypot, y_hypot)
print(f"hypot({x_hypot}, {y_hypot}) = {result_hypot}")

# Example for sin(x)
x_sin = math.pi / 2  # radians
result_sin = math.sin(x_sin)
print(f"sin({x_sin}) = {result_sin}")

# Example for tan(x)
x_tan = math.pi / 4  # radians
result_tan = math.tan(x_tan)
print(f"tan({x_tan}) = {result_tan}")

# Example for degrees(x)
x_degrees = math.pi  # radians
result_degrees = math.degrees(x_degrees)
print(f"degrees({x_degrees}) = {result_degrees} degrees")

# Example for radians(x)
x_radians = 180  # degrees
result_radians = math.radians(x_radians)
print(f"radians({x_radians}) = {result_radians} radians")
